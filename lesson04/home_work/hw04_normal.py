# Напишите скрипт, заполняющий указанный файл (самостоятельно задайте имя файла)
# произвольными целыми цифрами, в результате в файле должно быть
# 2500-значное произвольное число.
# Найдите и выведите самую длинную последовательность одинаковых цифр
# в вышезаполненном файле.
DIR = 'data'
file = 'long_number.txt'

long_number = ''
for i in range(2500):
    long_number += str(random.randint(0, 9))

if not os.path.isdir(DIR):
    os.makedirs(DIR)

with open(os.path.join(DIR, file), 'w', encoding='UTF-8') as f:
    f.write(long_number + '\n')

with open(os.path.join(DIR, file), 'r', encoding='UTF-8') as f:
    long_number = f.read().strip()

print(long_number)

result_match = []
pattern = r'(\d)\1{2,}'
for match in re.finditer(pattern, long_number):
    if match.group() not in result_match:
        result_match.append(match.group())
print(max(result_match, key=len))
